// פונקציה לקבלת לוח קל לפי מספר המשחק
export function getBeginnerGrid(gameNumber) {
  if (gameNumber < 1 || gameNumber > 10) {
    return null; // אם לא במסלול המתחילים, החזר null
  }
  
  return beginnerGrids[gameNumber - 1]; // מערכים מתחילים מ-0
}

// פונקציה לבדיקה אם משתמש הוא מתחיל
export function isBeginnerUser(gamesPlayed) {
  return (gamesPlayed || 0) < 10;
}